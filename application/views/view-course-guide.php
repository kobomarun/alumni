							<p class = "paragraph_content">Civil engineering is an engineering discipline that deals with the design, construction, and supervision of construction project in the public and private sector. Some of the infrastructures and construction work that are constructed by civil engineers include buildings, bridges, railways, roads, dams, canals, energy supply, airports, water supply, and waste management systems. It is also about maintaining these infrastructures and adapting them to meet challenges such as growth in population and demand, natural disaster, and also climate change.</p>
							<p class = "paragraph_content">There are two variants of civil engineers. The consulting civil engineers who generally focus on the planning and designing aspect of a construction project. They spend most of their time in the office and also liaising with clients to try to understand the requirements for the project. The second type is contracting civil engineers and what they do is turn the plan and design prepared by consulting civil engineers into reality. They carry out the actual construction work by translating what is in the plan and design into reality.</p>

